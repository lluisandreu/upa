		<h2>Kamal Badhey</h2>
		
		<h3 class="title">Portals and Passageways</h3>        
		
		<p><em>“Katha kanchiki, manam intiki”</em> is a Telugu phrase used in storytelling. It translates to “the story goes far far away, and now we are back in our homes.” <em>Portals and Passageways</em> has roots in the mythology and storytelling of families. The project pieces together elements of a dormant narrative of Annam Rathnaiah, the artist’s great great grandfather, a man who started a gold shop in 1884 in the General Bazaar of Secunderabad, India. The city originated as a British cantonment, with the bazaar serving the needs of the British army. Simultaneously, it became a place of commerce for Indians. Annam Rathnaiah, having come from farmer caste gained an urban identity when he opened a jewelry store in the bazaar. The wonderment of how he started the business has been mythologized and questioned by the Annam family.  </p>
		
		<p>The project <em>Portals and Passageways</em> is about the past, present, and future—reaching back to ancestors, but also looking forward to familial futures. The passageway of the work is a line that takes you through time and space, memory and story.  Each photograph can be entered and traveled. The collective of images is a meshwork of photographs, stories, and objects, constantly reformulated, disassembled and reassembled like a puzzle.</p>
		
		<h3>Biography</h3>
		
		<p>Kamal Badhey is a photographer, educator and visual urbanist from New York.  She received her MA in Photography and Urban Cultures from Goldsmiths, University of London and her MS in Museum and General Education from Bank Street College.  She has engaged with a variety of communities, but her most significant experience was as a visual arts teacher at Cypress Hills Community School in Brooklyn, New York, where she taught as a public school teacher for seven years. She has worked in the Exhibitions program and as a Lewis Hine Documentary Fellow at the Center for Documentary Studies, Duke University.</p>
		
