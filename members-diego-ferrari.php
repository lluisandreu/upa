		<h2>Diego Ferrari</h2>

		<h3 class="title">Urban Habitat II–BERLIN 2013</h3>
		
		<p>This project presents new photography and video documenting the work and music of Kingz of Kiez, a group of up-and-coming young rappers in Berlin interested in the collisions between the iconographic and the everyday within urban space. For this project I worked closely with these young people, taking them to iconic public places in Berlin in order to help facilitate a new set of expressions that challenge the usual clichés of rapper imagery and, by extension, migration, urban youth and disaffection. Through a playful and questioning approach, these images and video work invite the audience to rethink concepts of the inclusive multicultural city.</p>
		
		<p>This project is in partnership with: Puk a Malta - Kingz of Kiez, OKK Gallery Raum 29 and Kiez Kultur Netz – Berlin 2013.</p>
		
		<h3>Biography</h3>
		
		<p>Diego Ferrari is an artist and photographer based in West London. His recent work takes a fine art approach to street photography and the visualsation of urban life. His work interrogates the relationship between social values and public spaces, with a particular interest in the relationship between the body and its environment, articulating modes of individual and collective experiences and social relations. He studied Fine Art in Barcelona, completed a Fine Art BA at Goldsmiths, University of London and was awarded an MA in Art & Architecture at the University of Canterbury. He teaches the course on “Photography, Art and Architecture” at Central Saint Martins and on the BA in Photography at Kingston University.</p>
		
