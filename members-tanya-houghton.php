		<h2>Tanya Houghton</h2>
		
		<h3 class="title">A Migrant Tale</h3>        
		
		<p>‘A Migrant’s Tale’ brings together narrative explorations of home and nostalgia told through the language of food. The body of work represents a collaborative reworking of the migrant’s tale, exploring the relationship between the narrator and the interpreter, using photography to create a visual retelling of the migrants tale. As a result, ten Migrants living and working in London were interviewed about their relationship to food, narrating their stories of their motherland to the artist. In addition, exploring their use of food in daily routines and their ability to use this as an access point to their homeland.</p>

		<p>Through a decoding process their stories were reduced to a series of objects, sitting alongside family photographs from the participants. Still-life shrines were created through a visual reinterpretation of the stories they had told. The Shrine sits alongside a portrait of the migrant, accompanied by a text panel of quotations taken from the interview.</p>

		<p>The work challenges the concept of ‘home’ created through multiple grounding’s of fixed geographical points, exploring the notion of home as an amalgamation of rituals performed through routine practices of the everyday; practices through which one is able to perform a cognitive migration, travelling to and from an internal home carried within.</p>

		<h3>Biography</h3>
		
		<p>Tanya Houghton (Born.1985, Norfolk. England) is a social documentary photographer based in London. Having studied at Goldsmiths, University of London, her practice is informed by her background in urban sociology and constructed photography and intersects documentary photography and conceptual art based practice. Hougton’s ongoing desire to collect, compare and unearth the unexpected yet familiar details found in everyday life motivates her work. Through the exploration and documentation of daily networks, she explores the characteristics and memory of everyday communities, creating multilayered works that explore notions of home and memory.</p>

