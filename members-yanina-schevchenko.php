		<h2>Yanina Shevchenko</h2>
		
		<h3 class="title">Crossing Over</h3>
		
		<p><em>Crossing Over</em> is a photographic diary of Yanina’s trip across Russia on the Trans-Siberian Railway. Thousands of rail tracks reach out across Russia like threads of a giant spider's web. Even today, more than a hundred years after its construction, the Trans-Siberian Railway is at the core of Russia, for both transit and trade. The project introduces the train journey as a method for personal exploration of expansive landscape and offers a platform for rediscovery of what Russia is and what it represents. </p>
		
		<p>The Russian past and present, embedded in the scenery, have revealed themselves in glimpses through a particular lens: the window of a train. The photographs themselves and the process of creation in a sense produced new, meaningful places. Space, or in this case, the landscape of the country, has no substantial essence in itself, but is ‘a context for human life’. The vast distances and the journey across them, unlimited open space and people full of life and faith: these form the most genuine portrait of the country’s national space.</p>
			
		<h3>Biography</h3>
		
        <p>Yanina Shevchenko is a Russian-born photographer. She has worked in New York, Moscow and Buenos Aires and recently graduated from Goldsmiths University of London, MA in Photography and Urban Cultures. Her interests in photography are human identity, personal stories, urban and rural landscape.</p>
        
		<p>Crossing Over was recently published by the Velvet Cell and is available for purchase.</p>
		
