		<h2>Anastassios Kavassis</h2>
		
		<h3 class="title">Athenians</h3>        
		
		<p>This project focuses on cultural diversity and change in contemporary Athens. The capital city of Greece, Athens is the oldest city in Europe, originally developing from a small town to a substantial urban centre, attracting domestic and international migrants from local and global populations. Greece is considered to be one of the poorest countries in Europe and yet also has one the highest levels of migration, consequently experiencing the greatest social and cultural transformation in the history of the country.</p>
		
		<p>My work explores these cultural transformations through the lens of an old antique wooden camera, found in a basement in Athens; used in the past to copy art work and create 'printing stereotypes', the camera is now used to create unique non-reproducible portraits of Athenians, exposed directly onto printing paper, without using a negative. A series of 16x20 life size black and white photographic monographs are created to explore notions of diversity and difference, and to question existing stereotypes about what it means to be an Athenian today.</p>
		
		<h3>Biography</h3>
		
		<p>Anastassios Kavassis is a filmmaker and photographer who studied fine art at University of East London and fine art film and video at Central Saint Martins Art College. He has worked in a diverse range of media, from digital multi media and 16mm film to antique ultra wide format wooden cameras. Previously, a visiting lecturer on the MA in Photography and Urban Cultures and PhD in Visual Sociology at Goldsmiths, University London; Kavassis was also a course team leader on the Digital Media programme at Croydon College. His visual work has been funded by the Arts Council of England and distributed by the British council. He has an ongoing research interest in metropolitan cities and urban space, and has lived and worked in London, Hong Kong, Berlin, Athens and Amsterdam. Currently, he is based in Amsterdam and Athens.</p>
		
