		<h2>Gill Golding</h2>
		
		<h3 class="title">Walking The Greenwich Peninsula – A Case Study On Sustainability</h3>        
		
		<p>Within the short space of 150 years, Greenwich Peninsula has transformed from marshland to heavy industry, stagnated into dereliction, undergone decontamination and is currently experiencing significant regeneration to create a new, mixed use urban quarter.  Following the Brundtland Report (1987) and the Rio Summit (1992) when climate change and discourses about sustainable development became dominant global themes, strategies and policies were formed to provide a sustainable future for the Greenwich Peninsula.</p>

		<p>Greenwich Millennium Village, which sits at the southern end of the site, was the first millennium community to be created under the Millennium Communities Programme.  The ‘village’ was heralded as a flagship sustainable development that would serve as a template for a well-designed, twenty-first century, socially inclusive, mixed community.  It was envisaged that the construction of the village would meet an 80% reduction in energy consumption, 30% reduction in water usage, together with reductions in waste.</p>

		<p>Employing an immersive walking practice, ‘Walking the Greenwich Peninsula’ takes a critical look at the landscape and questions whether sustainable development practices have been achieved</p>
		
		<p>These images form part of a larger project that addresses sustainable development throughout the Greenwich Peninsula.</p>

		<h3>Biography</h3>
		
		<p>Gill Golding is an urban landscape photographer whose work adopts a geographical and sociological approach to critically engage with the urban environment. Her interest in post-industrial landscapes and the regeneration and gentrification of our cities is accompanied by questions about issues related to the contemporary city.  Her choice of walking as a research methodology allows her to encounter landscapes through an embodied practice, creating a dialogic relationship with the environment that encourages a critical approach to her work.  Gill completed an MA in Photography and Urban Cultures at Goldsmiths, University of London in 2015.</p>
		
