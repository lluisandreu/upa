		<p>THE LOUDEST CITY is an ongoing project documenting the participants and onlookers at various public gatherings on the streets New York City, in which I explore the relationship between individuals, communities and the larger city.</p>

		<h3>Biography</h3>
		
		<p>Konstantin Sergeyev was born in Odessa, Ukraine, and moved to New York City at the age of twelve. Early in his teens he began photographing bands and crowds at punk rock shows, developing an interest in subcultures, minority groups and protest movements. He has since photographed squatters, Zapatistas, war veterans and many other groups in the USA. He graduated Magna Cum Laude from Hunter College of the City University of New York with a BA in Studio Art focusing on Photography. His photographs have been published and exhibited worldwide, and have featured in New York Magazine (where he is a photo editor), VICE and the New York Times.</p>
		
