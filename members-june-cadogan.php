		<h2>June Cadogan</h2>
		
		<h3 class="title">A Portrait Of Grange Farm Estate</h3>        
		
		<p>Grange Farm Estate is the first part of a long-term project looking at the suburban regeneration of Grange Farm Estate, a council estate in the London Suburb of Harrow. Harrow Council announced their plans to demolish the estate in January 2015 as part of a larger regeneration strategy for the Borough. Since then the Council have been in consultation with local residents and businesses to take the plans for the regeneration of the estate forward to the next stage. The project will look critically at and investigate the effect that this ambitious regeneration proposal will have geographically, spatially and socially over the next few years.</p>

		<h3>Biography</h3>
		
		<p>June Cadogan has a degree in photography from the University of Westminster and a Masters in Photography and Urban Culture from Goldsmiths, University of London. Her most recent work is influenced by an interest in ‘community’ and the space inhabited by communities.
Her current project looks at the suburban regeneration of Grange Farm Estate, a council estate in Harrow, and will investigate the effect that regeneration will have geographically, spatially and socially over a five-year period.</p>
		
