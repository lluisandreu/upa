<h2>Santiago Escobar-Jaramillo</h2> 

<h3 class="title">London [Berlin-Shanghai-Bangkok-MexicoCity-Manizales], gap my mind, etc.</h3>

<p>This project has been photographed in London, Hamburg, Berlin, Hannover, Stuttgart, Amsterdam, Kenya, Tanzania, Sudan, Shanghai, Bangkok, New York, La Havana, Sao Paulo, Lima, Montevideo, Buenos Aires, Mexico City, Oaxaca, Manizales, Medellin, Bogota, Tabio (2003-2013). It is a long-term project that was initially presented on the MA Photography & Urban Cultures, at Goldsmiths College. The photographs invite us to see and experience the city in all directions: up and down, right and left. To pay attention and give importance to all the subjects (people and animals) and objects (buildings, trees, signs and varied objects) within the cities' landscape. </p>

<p>It is a reflection on the urban and the sociological relationships citizens can construct on the street or in their kitchens; in the public realm or the private sphere. Also, the way in which buildings and nature work in dialogue as a scenario that encapsulates human activities and emotions; those can be seen as objects that surround us, as they help or modify the atmosphere that embraces the sensorial apparatus of the body, and activates memory in our brain… and why not in our heart! </p>

<h3>Biography</h3>

<p>Santiago Escobar-Jaramillo is an architect at the National University of Colombia with a MA in Photography and Urban Cultures, Goldsmiths College, University of London. He has photographed for Villegas Editores, UNHCR, MFO, ICIPE, CUCR-Goldsmiths, in South America, Asia, Africa, Europe and United States. His creative work falls into the categories of artistic, documentary and urban photography, with more than 70 individual and collective exhibitions and he has earned various acknowledgments and distinctions. His project 'COLOMBIA, tierra de luz (Land of Light)' was exhibited at DRCLAS-Harvard University and MIT Community Innovators Lab in Boston, MA-USA. He runs photo workshops for Zona Cinco, Don Bosco, CANON, UNHCR and National Geographic Student Expeditions. He published the books Solidarity In Colombia, A Police Force For All Colombians, Bogota Apartments and London, Gap My Mind.</p>

		
