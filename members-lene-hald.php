<h2>Lene Hald</h2>
<h3 class="title">To (Un)veil and Envision</h3>

<p>These images are a study of Muslim identity constructions through dress. Through photographs, drawings and interviews the project sheds light on some of the experiences of young Muslim girls living in global cities in late modernity, specifically focusing on native Danish girls who have converted to Islam. To (Un)veil and Envision takes as a starting point that fashion and clothing is to be viewed as an important social sphere of identity construction, and the project seeks to highlight how fashion-practices provide a unique avenue for metaphoric self-expression and re-negotiation of discursive debates around Islamic dress. The project reflects on how important dress is in the creation and maintenance of identity and how changing patterns of migration, along with shifting gender roles and expectations, are made visible in their use of clothing. Through photographs and participatory drawing I aim to investigate fashion and dress as an intense personal and visual phenomenon expressing social identities and personal narratives.</p>

<h3>Biography</h3>

<p>Lene Hald’s photographic work explores the meeting between social research and narrative art and addresses issues of fashion, gender and identity.</p>

<p>She focuses on both a theoretical and art based practice and has studied for an MA in Photography at Goldsmiths, University of London (MA in Photography and Urban Cultures) and also holds an MA in Visual Communication from The Royal Danish Academy of Fine Arts – The School of Design.</p>

<p>She is currently studying for a PhD that explores how fashion studies and photography can be used as tools to understand, reflect and communicate issues of youth culture and identity construction.The PhD is made as a collaboration between KEA Copenhagen School of Design and Technology, Research and Innovation Centre and The Royal Danish Academy of Fine Arts – The School of Design.</p>

