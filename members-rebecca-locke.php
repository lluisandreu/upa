		<p>The themes of image and identity, the power of fashion, the myth of ‘self’, ‘place’ and one’s relationship to it, thread through this series by Rebecca Locke. Brooklyn // Bognor captures the artist’s experience of New York City in contrast to her roots by the English seaside. Her return to Bognor signifies a very different life, and an acute awareness of both unwritten social rules and the promise of ‘the city.’ In Brooklyn the artist’s practice was influenced by her performances as a singer in Sufjan Stevens’ band ‘Michigan Militia’, as a drummer in a Puerto Rican country music band and participant of the ‘electroclash’ fringe culture. Whether using photography, mixed media, creative writing or performance, Locke’s practice creates new narratives, often constructed as large scale, hyper-real compositions, saturated with colour. This series of twelve staged photographs utilises montage, performance and lens-based media—long exposures and sunshine, combined with digital layering to construct the narrative. The artist herself features in the images, wearing her Brooklyn clothes—her uniform of freedom—now worn in a very different context, the seaside town of Bognor. The images depict the charm and beauty of the English seaside, but hint at the uncanny, something of Brooklyn echoed in Bognor. A move to the city traditionally allows for the creation of a new narrative, a new self. Is it equally possible to reverse this, to create a new narrative of place?</p>
		
		<h3>Biography</h3>
        
        <p>Born in the UK, Rebecca Locke is based in Brooklyn which has proven formative in the development of her installation art, film, photographic, sound and performance-based artworks. She is a graduate of Goldsmiths, University of London, and has studied at the International Center of Photography and the School of Visual Arts in New York City. Rebecca exhibits internationally including the Festival de la Imagen, Manizales, Columbia hosted by the Universidad de Caldas, the Bienal de Fotografía, Lima and the Lab Film festival, London. She is a visiting fellow based in the Centre for Urban and Community Research, Goldsmiths, University of London, and a curator of the City to Sea Project collaborating in 2013 with Magnum Photos.</p>
        
