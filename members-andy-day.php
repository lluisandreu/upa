		<h2>Andrew Day</h2>
		
		<h3 class="title">Former</h3>        
		
		<p>In the late spring of 2015, I explored Former Yugoslavia’s communist-era monuments, in collaboration with parkour athletes from the region. Through the resultant images, the project examines collective memory; the monument, the ruin and the live site; culture deployed as governance (Yúdice) and the consequences of its subsequent failure; the counter-monument (Young); the potential of a new generation to establish an authentic sense of being within a contested present that is still emerging from a complex past; and how this sense of being can be achieved through the assertion of a new – if temporary – understanding of place.</p>
		
		<p>This project proposes that photography and edgework (Lyng) – and physicality more broadly – can combine to create interventions into sacrosanct space that allow a contemplation of the self in relation to the future, through embodied encounters with historicity.</p>

		<p>Through its inscription of the body onto the intensely visual notion of landscape, it brings a tactile and experiential reinterpretation of a space that emerges through an exploitation of the gaps found amongst a once-dominant spatial narrative.</p>
		
		<h3>Biography</h3>
		
		<p>Andy Day began photographing parkour in 2003. He has since worked around the world with elite athletes, including various founders of the discipline, on both commercial and personal projects. He now specialises in depicting creative, embodied, physical interaction with architecture.</p>

		<p>As a participant-observer, Andy is embedded within the parkour community and is immersed in the scene, continuing to play a role in shaping its visual culture. He recently received a distinction for his MA in Photography and Urban Cultures at Goldsmiths, University of London, taking a keen interest in architecture, urban morphology, creative cities, gentrification and cultural geography.</p>
		
