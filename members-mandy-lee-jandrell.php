		<h2>Mandy Lee Jandrell</h2>
		<h3 class="title">Constructed Leisure Environments </h3>
        
<p>I make photographs in environments that are primarily commercially orientated and include Theme Parks, Zoos, Safari parks, “Living” museums and the like around the world. </p>

<p>These environments operate within clearly defined boundaries.  Within those boundaries and separate to everyday lived reality, such environments concretise or materialise narratives into built landscapes. Hybrids of fact and fiction become presented as a concrete reality, almost fact, so that the boundaries between fact and fiction became blurred or confused. Landscape, presented within these environments, can be read as a very particular and subjective construction of ideas of nature. Nature, as represented by these contrived landscapes, is tamed, tidied up, seemingly safe. It is presented as a non-threatening and own-able object, in that the viewer can take possession of it.</p>

<p>Representations in these environments tend to reproduce pictorial conventions and so, tend to lean towards the nostalgic. What they produce are, often mythical, landscapes that are at odds with reality or the ‘real place’. But, while they look to the past in their use of idyllic conventions, they also look to the ‘No Place” or future of a perfect and desirable Utopian world. Like a nostalgia for a far away idyllic past, Utopia is often perceived as somewhere beyond, or out of reach of, our experience of the present or the real. What they attempt to produce in this juxtaposing of the nostalgic past and the utopian future is a sense of yearning, as if something is missing from the present, and this should fit very neatly into consumerist culture, which relies on a constant yearning or desire for something beyond the already attained or owned. </p>

<p>The photographs in this series were made in the UK, France, Spain, The Netherlands, the USA and Hong Kong.</p>
		
		<h3>Biography</h3>
		
<p>Born in South Africa, Mandy Lee Jandrell is an artist now based in the UK.  Jandrell'’s photographs been shown both in the UK and abroad, including The Serpentine Gallery, The Whitechapel Gallery, the Royal Academy of Art, London and the South African National Gallery in Cape Town. Her work is in several collections in the UK and South Africa, including the Government Art Collection (UK) and The South African National Gallery Collection. </p>

<p>Her work was most recently published in Edge of a Dream: Utopia, Landscape and Contemporary Photography.</p> 

<p>She completed her MA Fine Art at Goldsmiths College in 2003 and is currently Senior Lecturer and Course Leader of Photography at Southampton Solent University. </p>


<p>Jandrell’s work had been published and reviewed in publications including The Guardian, The Observer, Art Review, Art Monthly, Flash Art, Contemporary, Alaska, Fleisch (Austria), Art South Africa, Arena, Dazed and Confused, Artthrob (South Africa), Itch (South Africa), and the Mail and Guardian (South Africa).</p>

