		<h3 class="title"><?php echo $title ?></h3>

		<p>This series is part of a body of work that explores the ‘traces and aura’ of my own house through its demolition space-time. The project gives insight into how the moment of destruction reveals a greater history of complex socio-cultural, psychological and physically embodied meanings. As Walter Benjamin writes:</p>

		<blockquote>&ldquo;The trace is appearance of a nearness, however far removed the thing that left it behind may be. The aura is appearance of a distance; however close the thing that calls it forth. In the trace we gain possession of the thing; in the aura, it takes possession of us.&rdquo;</blockquote>	
			
		<p>Benjamin thinks about ruins as ‘petrified life’ visibly bearing ‘marks and wounds of the history of human violence’. The hammer (a ready-to-hand tool), which destroys my house, also damages my body insofar as the house was already an indication of my body. As the hammer smashes the house and its elements, it can never smash it in the same way again. The relation of value and significance of that blow is seen as the aesthetic moment - the collapse of subject-object and time-space-place in spatial unmaking. The break is an opening, an in-between-ness of becoming.</p>
		
		<p>In collaboration with Santosh Thorat and Niti Gourisaria.</p>

		
		<h3>Biography</h3>
		    
        <p>Vrinda Seksaria graduated from the Faculty of Architecture, CEPT, India; has PG Diploma in Indian Aesthetics, Mumbai University and MA in Photography and Urban Cultures, Goldsmiths, University of London. She has exhibited as part of group shows – Crossing Lines 2011, London; Liminal 2011, Old Truman Brewery, London and Experience Mumbai 2006, Mumbai. She is currently a visiting faculty at Sir J.J. College of Architecture and KRVIA, Mumbai where she is doing a fellowship titled ‘Traces and Aura’. Her work explores links between architectural-urban studies, socio-cultural research and photographic practice; with a focus on cultural archaeologies of urban space and the politics of architectural memory, construction, demolition and decay.</p>

