		<h2>Nora Alissa</h2>

		<h3 class="title">Albalad - A Blurred Perception</h3>
		
		<p>My photographs flow swiftly from previous projects concerned with space, and the urban environment. The images in this project are about the more traditional areas in Saudi Arabia, alien to the modernized rural cities in the Kingdom. The character of these places become humanized, taking on a body of their own. The human figures become without an identity, distant figures in the midst of the rural space. Rural space takes on a central character in the narrative of Saudi Arabia. People become eaten-up by the old buildings, the large trees and the traditional objects on sale in the souk. Their figures are wispy bodies, transient aspects of a space that is infinitely timeless, almost like it exists outside time.</p>
		
		<p>Hazy bodies and blurred subjects reflect the thought process of this work. Departing from a photography that seeks to depict reality, the images here are more intuitive and emotional. The very act of exploring urban space and travelling within it becomes a random and personal act. My approach is not predetermined and constructed, giving room for chance to play an essential role. What I see a the imagesâ€™ lack of superficiality, deem the photographs to be less about framing and aesthetics than they are about the human perception of the lived environment. In the images, it is not my role as the artist to impose the viewed subject; the reader alone is in charge of stabilizing a very blurred perception.</p>
		
		<h3>Biography</h3>
		<p>Nora Alissa graduated from Goldsmiths, University of London with a distinction MA in Photography and Urban Cultures. She is now working on several projects that look at different aspects of lived experiences, including the everyday, space and culture. She also works as an art consultant in the Kingdom of Saudi Arabia, helping to promote visual arts in the country.</p>

