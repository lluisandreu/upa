		<p>This series documents the retracing of the steps of their journey using the personal account documented in their photo album. The album records the couple’s travels to Switzerland between late August and early September that year and comprises original, caption-dated photographs and an accompanying map that traces their route. Sporadic captions slowly reveal the location of each image and presented Killeen with the intriguing possibility to recreate their journey.</p>
		
		<p>In 2010, 75 years after the couple’s trip to Switzerland, another journey was undertaken in an effort to trace these sites and photograph them once again. In a unique approach, Killeen documented the original locations featured in the album and photographs were taken at the same sites, on the exact dates and in the same order, three-quarters of a century later.</p>	
			
		<p>This anonymous album purchased for less than £10 has provided a distinctive opportunity to illustrate a particular style or method of photography; of recording a past event, similar to the archaeological process of using fragments of information as evidential clues. The forensic approach retracing of steps in the couples’ journey highlights a multitude of changes that can be observed today in the environmental, historical, and sociological landscape.</p>
		
