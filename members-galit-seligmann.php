<h2>Galit Seligmann</h2>

<h3 class="title">Pilgrimage</h3>

<p>Contemporary society is a mobile society, and John Urry in his book The Tourist Gaze, describes the modern day tourist as a contemporary pilgrim. A tourist queue is the concentration of this mobility, and the intention of the series Pilgrimage, has been to invert the tourist gaze at two of the most visited sites In the Eastern and Western world: The Taj Mahal in India, and St Peter’s Basilica in Italy. The buildings are described not by their material structure, but rather by the tourists visiting them, and all identifying context has been excluded from these two endlessly reproduced landmarks. As a result of the ease of travel, tourist sites across the world have acquired a same-ness about them, and this neutral backdrops symbolise the ubiquity of the modern day tourist trail.</p>

<p>The traditional and contemporary pilgrim meet in the space of St Peter’s Square and the Taj Mahal, with a mix of package tourists, religious tourists, independent travellers and families. A queue is a procession, partly of national identity and demographics, and partly of dress code and body language. It is a temporary community, whereby a collective spirit of anticipation exists in between the boredom of waiting. Strangers have one thing in common: they are all waiting for the same thing. While in this temporary state of waiting, the action of photographing has become a habitual aspect of the tourist gaze, and many use photography as their frame of reference for looking at the object they have come to admire. </p>

<h3>Biography</h3>

<p>Galit Seligmann grew up in Johannesburg and Tel Aviv, and has been based in London since 1997. With her professional background in Architecture, she uses photography as a means of understanding and expressing the urban environments she encounters, as well as exploring contemporary urban issues.  In 2013, Galit completed her MA in Photography and Urban Cultures at Goldsmiths, University of London.</p>

