		<h2 class="title">Yanni Eleftherakos</h2>
        
		<h3><?php echo $title ?></h3>
        
        <p>These images are part of an ethnographic project looking at how spaces transform from personal sanctuaries to work places. The project develops on three axes. Firstly, all participants are young homosexual men, confronting and conforming to mandates of various –normativities, such as heteronormativity. Secondly, all participants are non-British, perceiving and performing domesticity by trying to sustain elements of their original cultures and juxtaposing conditions between their hometowns and London, a city with a reputation for overpriced and restricting housing. Lastly, all participants receive clients in their places to offer massage and/or sex services, constantly negotiating their safety, their privacy and their authority over what people are used to call home.</p>
        
        <p>The discussion around private and public space finds a new line of argument in this photographic work, since it compels the viewer to reconsider what can and cannot be identified as public or private, along with the criteria for such distinction. This aspect was persistently under consideration at all stages of the photographic involvement; framing, shooting, selecting, editing, printing etc. However, that was solely on the part of the photographer. The manner of the participants, or hosts, strongly suggested they had nothing to hide and in this way the researcher, or guest, converted from intruder to friend naturally and in a flash (of the camera).</p>
        
        <p>At the same time, the project, through all its axes, wishes to remind the viewer of particularly important facets of what we call “urban life” and, by focusing on personal objects or decorative elements, it builds spatio-temporal and symbolic bridges between the outside and the inside, the bedroom and the street, the present and the past, London and other locations. The city is as much at train stations, office buildings, or markets as it is in cupboards, under beds and on bathroom tiles.</p>

		<h3>Biography</h3>
		
		<p>Yanni Eleftherakos studied Photography and Urban Cultures at Goldsmiths, where he is now pursuing his PhD in Visual Sociology. He gives workshops on photography and designs urban photographic walks. Always working in and with cities, he is particularly interested in the connections between place and class, as well as the ‘genius loci’ and urban identities. He mostly creates and shows his work in London and Athens. He works with The National Citizen Service mentoring young people on community issues and is the programme coordinator of Urban Photo Fest in London.</p>
		
