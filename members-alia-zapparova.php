		<h2>Alia Zapparova</h2>
		
		<h3 class="title">Days</h3>        
		
		<p>This series takes its departure from a question about ways of inhabiting space and discovers how a space becomes a habit. It looks for a correspondence between taking photographs and living at home, between collecting images and finding ways to occupy domestic space. The images embody repetition and habit: different daily responses to everyday space, which remains the same but is always lived in slightly differently from day to day. Like days of the week, they structure a life without determining it.</p>
		
		<p>The question becomes a search for the everyday at the limit of its perceptibility, concerned with a time and a space to the side of the events and activities of daily life, where nothing happens. The work aims to catch stillness by means of repetition; to follow the process of interior space dissolving into time. It seeks to establish a poetic and photographic identity between daily time and light, between the everyday and the light of day, the daylight in which we live.</p>
		
		<h3>Biography</h3>
		
		<p>Alia Zapparova a photographic artist with a background in philosophy. She was born in Moscow and lives in London. She hold a BA in philosophy from the University of Cambridge and an MA in Photographic Studies from the University of Westminster. She make books and installations composed of photographs; her subject matter is the everyday at the limit of its perceptibility.</p>
		
