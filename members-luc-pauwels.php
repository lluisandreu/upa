		<h2>Lau Pauwels</h2>
		
		<h3 class="title">Thick Streets</h3>        
		
		<p>Sociology and photography represent distinct ‘ways of looking’ at society. Yet they are both focused on ‘making the familiar strange’, through interrogating the seemingly obvious or incidental for their deeper layers of significance and by ‘reframing’ them as (visual) statements, observations or ‘data’ of sorts. Visual sociologists and urban photographers in particular share the fascination with the everyday as enacted in cities around the globe. The urban social fabric in constant flux becomes apparent through observing and depicting routine behavior, incidental acts, and through scrutinizing the various signs and symptoms of how the city is ‘used’ by a colorful diversity of inhabitants and visitors. The day-to-day metabolism of the city also reveals itself through its artefacts which are as much materializations of norms and values as objects that are constantly being uploaded with new meanings, or re-appropriated to fulfil new functions. This selection of digital colour images focusses on cities as transient repositories of cultural signifiers and multi-authored canvasses of change, power and resistance. As practice-based musings on visual ways to know and experience cities, they represent but a small sample of a much greater and ongoing body of analogue and digital work on different aspects of globalizing urban cultures.
</p>

		<h3>Biography</h3>
		
		<p>Luc Pauwels is a Professor of Visual Research Methods and the Director of the Visual & Digital Cultures Research Center at the University of Antwerp, Belgium. His recent monograph ‘Reframing Visual Social Science: towards a more visual sociology and anthropology’ (2015, Cambridge University Press) exemplifies his long-standing involvement with the visual study of culture and society. An avid photographer since early childhood, the focus of Luc’s visual work is on urban culture and the semiotics of street life. His photographic work has been awarded, shown in solo and group exhibitions and published in magazines and journals.</p>
		
