		<h2>Nai Wen Hsu</h2>
		
		<h3 class="title">(un)Lucid (non)Dream</h3>        
		
		<p>This series of images were made in London in the winter of 2015, representing a stream of consciousness in their portrayal of urban night walks.</p>
		
		<p>When night descends, it gradually obscures the daytime landscape and impinges on one’s mobility around the city, revealing hidden walking trajectories and qualitative possibilities. Night walking provides an alternative and intimate chronicle of urban experiences, and by navigating through shifting environments I established some regular detours. It seems that night walking does the work of ‘making’ and ‘meaning’ at the same time, and this elusive magic resides within a seemingly infinite space where only one step across the threshold feels like moving into a lucid dream-state.</p>

		<p>These images are not based on linear narrative; rather, they are signs of roaming, of gazing, of sparks between awareness and intuition, of a freedom, a metamorphosing, of the experienced and the abstract. These fragmentary moments, while too easily ignored in datytime reality, belong to the organism of urban space and speak to urban experience as a whole. By adopting an ethnographic approach, the project also seeks a quality of silence, an alternate perpetualness; and with respect to day-night cycles, these images reflect a spontaneous micro perspective around the daily life of a foreign observer making a visual story that aims to be both poetic and reciprocative in manner and approach.</p>
		
		<h3>Biography</h3>
		
		<p>NaiWen Hsu / 許乃文 was born in Taichung, Taiwan in 1991. She received her MA Photography and Urban Cultures from Goldsmiths, University of London in 2015, and is interested in urban theory and interdisciplinary debates. Her work is often black and white, reflecting a mixture of visual approaches to landscape, architecture, portraiture, object, sensation, movement, and some blurred impressionist urban visions that are ambiguous in meaning. By navigating city spaces, she asks questions about how such encounters engender a more philosophical approach to fear.</p>
		
