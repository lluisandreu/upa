		<h2>Camilo Jose Vergara</h2>
		
		<h3 class="title">By Night in Los Angeles</h3>
        		
		<p>Curiosity led me to get up at three in the morning. I was eager to see how my new digital camera, a technological marvel, would capture the quintessential LA inner-city scenes that so captivated me during the day. </p>
		
		<p>For weeks I drove at night through the streets and alleys of Skid Row, South Los Angeles, and Pico Union. I wanted to see if the sinister structures of Skid Row would be as forbidding in the dark as they were under the sun. Small, quirky fortresses made me stop--fences and spikes illuminated from behind, their shadows projecting onto the street--something like the darker side of Disney.  Whenever I encountered a familiar form, I climbed up on the roof of the car in order to include more of the ground in the photo and to help me center the view.</p>	
			
		<p>Not stars, but electric lights, illuminate LA's nights. Lights coming from the buildings I was photographing invited me to enter. So, too, streets and alleys lit with patches of yellow tungsten, white fluorescent lights, and the colorful neon lights of businesses.</p><p>Cats were awake and about. Gang members and dogs slept. I saw many working people waiting for busses, and seemingly oblivious to all about them. I looked for police cars, the homeless, and the insane and when approached by harsh faced prostitutes I kept moving. Once, while I was stopped to change lenses, a street walker stuck her face in the open window, frightening me with her dead eyes.</p>
		
		<p>I liked the silence and felt that something momentous was about to happen, all but expecting to see the Four Horsemen of the Apocalypse galloping along the avenues, or to brake for the Terminator crouched naked in front of my car. At the same time I was mesmerized by a sign advertising a two-pound double king burger at The Best Burger on South Western Avenue.</p>
		
		<p>Around 4:30 the black sky began to turn blue, as dawn lightened the distant sky and the enormous city of Los Angeles started to wake up. Tired, excited, and disappointed with night's ending, I went home to sleep.  But vivid scenes from my  nocturnal adventure kept me awake.</p>
		
		<h3>Biography</h3>
		
		<p>Camilo Jose Vergara is a Chilean born photographer and writer. His subjects are: representing time, the American ghetto, ruins, and American popular culture. His works have been widely exhibited, most recently in the New York Historical Society (<em>Martin Luther King Jr.:  The Dream Continues</em>, 2013), the Museum of the City of New York (<em>Tiny Towers: 1970–2011</em>, 2011–12), and the National Building Museum, Washington D. C. (<em>Detroit Is not Dry Bones</em>), 2012; How the Other Half Worships, 2009). Amongst his publications are Harlem: The Unmaking of a Ghetto (forthcoming, 2013), <em>How the Other Half Worships</em>, 2005, The <em>New American Ghetto</em> (1995), and <em>Twin Towers Remembered</em> (2001). In 2002 Vergara was awarded a MacArthur Foundation "genius" grant and he was awarded the National Humanities Medal by President Obama in 2013. </p>

		
