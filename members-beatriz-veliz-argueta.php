		<h2>Beatriz Veliz Argueta</h2>
        
		<h3 class="title">Notes from a post-Soviet space: On Georgia and the landscape of aftermath</h3>

		<p>This project questions the changing architectural landscape of Tbilisi, Georgia as part of the new political discourse that is in the process of reshaping the city’s landscape. The urban landscape has thus turned into a space of encounters: of new and old buildings; of the legacies of soviet times and the challenges of new democratic times.</p>	
		
		<h3>Biography</h3>	

		<p>Beatriz is a storyteller that weaves images and text in an effort to create new ways of reading the world. Her work is an ongoing dialogue of ideas, experiences, anecdotes and fragments of life shared with others…</p>
		
		<p>Her work – combining social theory and photography – moves between documentary and art. She focuses on exploring the everyday in remote communities and post conflict societies.</p>
		
		<p>Her work has been recognized by the Prix de la Photographie Paris and exhibited in Germany, Guatemala, South Africa, India, France and Spain. She is Associate Research Fellow at the Center for Urban and Community Research (CUCR), and Co-founder and Coordinator of the international Urban Photography Summer School at Goldsmiths, University of London. Beatriz is also Photo Editor of Bifurcaciones, Revista de Estudios Culturales Urbanos, Universidad Católica de Maule, Chile and also an Advisor to Urban Photo Fest.</p>


