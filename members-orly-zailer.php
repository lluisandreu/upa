		<h3 class="title">The Time elapsed Between Two Frames.</h3>

		<p>In this ongoing project I have reproduced old photographs from various family albums with descendants of the people photographed in the original ones. I have chosen people with significant physical resemblance to their antecedent.</p>
		
		<p>People we regard in old photos, which were photographed using old fashioned analogue cameras, had different fashions, different life styles and even in some of the cases different cultures. Yet, their performativity is common to the present. All people when observed by others, especially through the lens, put on an act in order to create an ideal representation of the self to society and to future generations.</p>
		
		<p>I started this project with a self-portrait with my partner, reproducing a photo of my parents which was taken 40 years earlier, as I have been fascinated with extreme resemblance of people to their parents in general and mine in particular. I wanted to perform the performance my parent did so long ago as if to actually be there and become a different person.</p>
		
		<p>This project deals with questions of identity in the shadow of our antecedents as well as physical resemblance passed forward and the extent to which it affects our personality.</p>
		
		<h3>Biography</h3>
		
		<p>Orly Zailer is a documentary and fine art photographer. She graduated from WIZO Academy of Design, in 2008 with B.Ed.Des in photography and education. For several years, she worked as a freelance photojournalist as well as a photography teacher. She completed a master degree in Photography and Urban Cultures at Goldsmiths, University of London in 2012.</p>
		
		<p>In the past few years she has been focusing on Environmental Portraiture of various people from different groups and circles in society mainly in Israel and the UK. Some of her projects were exhibited in various group exhibitions. At present she lives and works in London.</p>
		
