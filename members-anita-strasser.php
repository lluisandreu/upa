		<h2>Anita Strasser</h2>
		
		<h3 class="title">Deptford high street: a visual ethnography of everyday life on a high street in south-east london</h3>        
		
		<p>This project aimed at utilising photographic research to gain local knowledge and create a sense of belonging in the area I had just moved to by encouraging shopkeepers, customers and residents to share their experience of this street. Lived experience is deeply embedded in history; even if interpretations are subjective, as psychological truths these accounts are just as important as factually reliable accounts. Story-telling also reduces the gulf between me, the stranger, and them, which is necessary in building relationships with the people whose history is being talked about. The result is a collection of images and texts that seeks to combine my visual interpretations with the textual documentation of people’s lived experience (the texts can be read on my website).</p>
		
		<p>My practice is an attempt to counteract the widespread practice of Street Photography where photographers refrain from any kind of engagement with their subjects and make unsubstantiated claims about people’s ‘realities’. Deptford High Street is a favourite spot for photographers looking for the bizarre, with many residents frustrated with the misrepresentation of their lives. Through active engagement with my subjects, I aim to give participants a shared power in defining their own collective past and representing their community.</p>
		
		<h3>Biography</h3>
		
		<p>Anita Strasser is an urban photographer / visual sociologist currently studying towards a PhD in Visual Sociology at Goldsmiths, University of London. Her photographic projects document social histories through visual ethnographies, and utilise participatory photographic research practice and story-telling as a means of encouraging social interaction among participants and giving participants voice in the representation of themselves. Her research interests are urban communities, the representation of class, social and spatial inequality, regeneration and gentrification, visual research methods, oral histories and participatory photographic practice.</p>
		
