		<h2>Anne Zeitz</h2>
		
		<h3 class="title">Extractions</h3>        
		
		<p>Extractions (2012) is a photographic and video installation produced in collaboration with David Boureau. It is based on the localisation and measurement of former uranium mines in Germany and France. The work documents traces of the economic, political and cultural history of the two countries related to the extraction of uranium. It reveals the diversity of the zones and sites, which are more or less close to cities and their suburbs and more or less abandoned or rehabilitated. A second part of the installation focuses on the representation of methods for measuring radioactivity. Photographs from scientific archives from the 1950s to the 1960s show specialised operators in the process of taking measurements. This series of images illustrates pieces of scientific apparatus and gestures related to the way nuclear radiation has been perceived in the past and the way we perceive it today.</p>
		
		<h3>Biography</h3>
		
		<p>Anne Zeitz is a researcher and artist working with photography, video, and sound media. She is Associate Professor at the University of Rennes 2 (France) and has been teaching at the University of Paris 8. Her doctoral thesis from the University of Paris 8, Esthétique, Sciences et Technologies des Arts, 2014, is entitled (COUNTER–)OBSERVATIONS, Relations of Observation and Surveillance in Contemporary Art, Literature and Cinema. Anne Zeitz has collaborated with the Goethe-Institut Paris and the Goethe-Institut Johannesburg. She is a former member of the Observatoire des nouveaux médias (University of Paris 8 / ENSAD) and is part of the research projects Média Médiums (University of Paris 8, ENSAPC) and La fabrique des arts sonores (University of Paris 8, Centre Pompidou Metz). She was Festival Artist at the 2014 Urban Photo Fest (London) and received a fellowship from the Institut national d’histoire de l’art and the Institut Français in collaboration with the HGB Leipzig in 2016. </p>
		
