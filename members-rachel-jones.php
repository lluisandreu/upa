<h2>Rachel Sarah Jones</h2>
<h3 class="title">Landscape of Disappearances Series</h3>

<p>This series addresses the connection between light and the perceived materiality of the urban environment. Light is examined through reflection to reveal nuances of texture, colour and form within the urban landscape. Abstract studies of light have been produced from an intervention – coloured filters inserted into the landscape reflect light within the urban environment. This light has then been reflected again and translated through the photographic process, resulting in an alchemical transformation of subtle hues and shapes. The images within the series are made up of montage – two or more photographs overlapping to create a composite – building up layers of the landscape, signaling the layering that takes place within the process of perception of the urban environment. The abstraction that occurs through reflection and montage references the time-space compression within the global city (due to the speed of mass transportation systems, digital information technology and other devices), which causes the material landscape to 'disappear', effectively erasing physical space. The boundaries between immaterial states of being (affect, imagination, the dream-state, phantasmagoria) and the physicality of the urban landscape become blurred within these images.</p>

<h3>Biography</h3>
<p>Rachel Sarah Jones’ work explores photographic and filmic approaches to urban space and temporality. She has exhibited her work in New York, Glasgow, London, and Washington, DC, including Color Field Remix in conjunction with the Kreeger Museum. Rachel has participated in numerous international symposia and festivals, including events at the British Library and Parasol Unit, and most recently she will be presenting her work at the Urban Encounters symposium at Tate Britain. Her photographs are held in the permanent collections of the University of the Arts London, Clerical Medical Corporation, and various private collections. She is currently a Post-Doctoral Teaching Fellow at Goldsmiths, University of London.</p>

