		<h3 class="title"><?php echo $title ?></h3>
        
        <p>In Dubai, UAE capitalist free-market ideals and publicity techniques are woven with autocratic state controls that organise architectural expansion and manage the spatial flow of migrant workers in the city. Highways and roads dominate the landscape, and within his infrastructure walking is discouraged; roads become walls, boundaries and lines to be navigated within the cityscape. Always Let the Road Decide explores photographically how South Asian construction workers who have limited social rights of use within the city independently access and appropriate these busy networks on foot. Climbing up onto the road allows individuals to dictate the pace of their collective movements within a hostile environment / cityscape. Male workers create informal activities meeting points or communal spaces and generate collective spatial practices in between highways, on undeveloped plots and in cultivated gardens. Consequently 'temporality' becomes an important structural component in forming ocular identity in Gulf cities. Furthermore improvised 'diversions' in space produced by the transnational and localised movement of people, could introduce a fresh spatial dynamic to city spaces that have outgrown their original purpose. Therefore producing research opportunities to explore how the unregulated social activities of low paid male South Asian expatriates could influence planning and construction procedures within the Gulf region.</p>

		<h3>Biography</h3>
		
		<p>David Kendall's practice explores how spatial, economic and design initiatives, as well as participatory practices, combine to encourage social and spatial interconnections or conflict in cities. Kendall utilises visual archives, mapping, events and embodied experiences to activate and generate his photographic, film and site-specific projects. His photographs, spatial research and collaborative projects have been exhibited and presented internationally including the British Museum, UK, Jüdisches Museum Berlin, Germany, Centro Cultural Manuel Gómez Morín, Santiago de Queretaro, Mexicó, Tate Britain, UK and Institut français London, UK. Kendall is a visiting research fellow within the Centre for Urban and Community Research, Goldsmiths, University of London, UK.</p>

