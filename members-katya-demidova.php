		<h2>Katya Demidova</h2>
		<h3 class="title">The end of the World </h3>
        
        <p>I never believed in the end of the world, we have survived so many it was hard to think the one of 2012 would be any different. And yet some strange superstition was creeping on me. What if the world comes to an end and I still haven’t seen much of it!</p>

		<p>So I set on a journey to South America. On the road I dreamt of shipwrecks and a comet crashing the Earth. I saw dinosaurs’ footprints, swam in salt lagoons and explored deep canyons. I was falling. Falling in love with the world and myself.</p>
		
		<p>They said we shouldn’t take ‘The end of the world’ too literal this time, that it would be more of a spiritual transformation, transition onto a new level of perception and consciousness. In a sense it did happened to me. I feel like I ‘died’ once or twice and will never be the same. At some point of my journey I returned home to say goodbye to my grandmother who was fighting cancer for many years. Her death left me in an emotional coma for months. Three months after I decided to continue my journey and flew back to South America. </p>
		
		<p>It has been a year of great discoveries and encounters and also a year of many goodbyes, a year of separations with people I loved, with people I met on the way. An amazing lesson learned: to be able to walk away from things that don’t make me happy anymore. </p>
		
		<p>This series is part of the book I am working on at the moment; it’s a visual personal diary of a life I lived in 2012 when I fell in love with the world and lost fear of losing it. </p>


		<h3>Biography</h3>
		
		<p>Growing up in an artistic environment encouraged Katya to engage with art and photography since a young age. She moved to London in 2005 after completing her degree in linguistics at Moscow University of Foreign Languages. She took a postgraduate degree in Fine Art from Byam Shaw School of Art /University of the Arts London/ where she explored various forms of art such as performance and installation. However, photography remained her main field of interest. Being interested in documentary photography and storytelling she continued her journey at the University of Westminster undertaking a master degree in photojournalism. In her work Katya touches on issues related to social aspects such as youth, ageing, and cultural identity.</p>
		
