		<p>This photographic project was made between 2002 and 2012. The images were produced in response to a series of walking detours around cities and urban spaces such as Berlin, Shanghai, New York, Trieste, London, Las Vegas, Bogota, Brighton and Margate as a result of a number of unplanned and accidental encounters. My main purpose was to use the camera to consider what it means to ‘not know a place’. Whilst walking through these spaces, I realized that there was something sociologically and philosophically unsettling about finding oneself in a city, urban or rural space that one had not anticipated being in, without any meaningful prior knowledge. As such, this caused a degree of disruption around notions of ‘geographic certainty’ and notions of placed-ness located within a conception of stable and linear history. This in turn became an informal methodology by which ideas of ‘somewhere-ness’ and ‘nowhere-ness’ were revisited and rethought through an ongoing visual praxis that involved the possibilities of being unintentionally lost.</p>
		
		<h3>Biography</h3>
		
        <p>Paul Halliday is a photographic artist, film-maker and urbanist who originally trained at the London College of Communications and Central Saint Martins Art School. He went on to study social anthropology, art history and archaeology at Goldsmiths, and the Universities of Oxford and Cambridge. He was formerly a media adviser at the British Refugee Council and has been the course leader of the international MA in Photography and Urban Cultures at Goldsmiths since 2002. He is also a director of London’s Photofusion, convener of the International Urban Photography Summer School and creative director of Urban Photo Fest working in partnership with Tate Britain and the International Association of Visual Urbanists.</p>
        
