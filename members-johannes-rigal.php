<h2>Johannes Rigal</h2>
<h3 class="title">Urban Women – The Urban Ads Project V02</h3>

<p>In this ongoing project I aim to show the effect that advertising in urban spaces can have – visually, aesthetically and politically. The city is and always will be filled with billboards and posters, putting forward opinions and thoughts, the mundane and the dream, stereotypes and cliches. We are often exposed to harsh campaigns, while we are never sure if what we see has any reference point with reality. The fact is that we are surrounded by images promoting “something“ for “someone“.</p>

<p><i>Urban Women</i> is the second part in a trilogy on the images that transport such messages. The first part – Shanghai Billboards – plays with the same idea but in a typographic, almost encrypted way. <i>Urban Women</i> is much more obvious, aggressive – just the way the female image is used very often in advertising. The (re)presentation of women in such ways and female images created for advertising fill the streets of cities nowadays. It is time to analyze these images critically in order to rethink and engage.</p>

<h3>Biography</h3>

<p>Johannes Rigal is a visual sociologist, photographer, curator and workshop teacher. He completed a BA in Development Studies in Vienna before deciding to combine photography and social sciences. At Goldsmiths, University of London, he completed an MA in Photography and Urban Cultures in 2011. He is based in London, Berlin and Vienna and is currently a Visiting Fellow at Goldsmiths College. He works as part of Gasket-Gallery and is a coordinator of Urban Photo Fest.</p>

