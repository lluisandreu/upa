		<h2>Stefano Carnelli</h2>
		
		<h3 class="title">Transumanza</h3>        
		
		<p>Transumanza is the seasonal movement of livestock between summer and winter pastures. In northern Italy this tradition is still practiced by a small group of shepherds who move their sheep from the Alps to the highly urbanised Pò plain in Autumn, and back to the mountains in Spring.
Unexpectedly, the economic force that keeps Transumanza alive is no longer the wool trade, but the increased demand for lamb and mutton from the growing local community of Muslim immigrants.</p>

	<p>The journey of the shepherds, in often portrayed via the imagery of bucolic pastolarism, yet this project aims to reveal a complex and often conflictive relation between their fragile and marginal nomadic community and the densely populated region it cyclically passes through.
This project follows the irregular lines drawn on the territory by the continuous displacement of the shepherds and their livestock, suggesting an alternative mapping of this complex urban and social environment.</p>

		<h3>Biography</h3>
		
		<p>Stefano Carnelli was born in Milan and lived in Lisbon and Barcelona before moving to London in 2013. His background in architecture and urban planning, have shaped his belief that cities eventually end up forming themselves, in a continuous transformation process that deeply affects concepts like identity, belongingness and sense of community. </p>

		<p>Carnelli’s desire to understand and document this phenomenon is the reason he started using photography to implement his theoretical work. Photography, Carnelli believes, is the most effective tool to capture the essence of the transformations we endure, deciphering the complexity of the world we live in.</p>

