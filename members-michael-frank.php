		<h2>Michael Frank</h2>

		<h3 class="title">23_05_2007</h3>
		
		<p>My ongoing photographic research work, takes its cue from the words of Roland Barthes’s Camera Lucida – ‘Since the Photograph is pure contingency and can be nothing else (it is always something that is represented).’ </p>
		
		<p>What can we learn in means of self-representation, and can the ‘self’ actually be fully represented, from anonymous faces and gestures in the crowd? Can we extract an essence from the representation of a human face depicted on any photographic media?</p>
		
		<p>What is the interaction or rather the interference of the photographer with his or her subject? Is the photographer acknowledged and therefore adding a further layer to an ever-changing equation? Having shot the whole project with a concealed camera or as Wordsworth would put it; ‘he dons his coat of darkness’, and always on the move in between a buzzing multiplicity, the resulting photographs give us a close look at the ‘face’. Given this unawareness of being portrayed, can we assume that we see a true representation of our inner self in a public space?</p>
		
		<h3>Biography</h3>
		
		<p>Michael Frank is a commercial photographer with over 20 years of studio and location experience. After many years of freelance corporate, advertising and portrait work with national and international clients Michael moved to London and subsequently took a number of short courses on photography, art and architecture at Central St. Martins under Diego Ferrari. This led to further academic study through the MA course “Photography & Urban Cultures” at Goldsmiths University of London. As part of his successful dissertation and final visual project he started the project named 23_05_2007.
		Michael recently co-founded Gasket Gallery, an international the online-based photographic space. </p>
		
