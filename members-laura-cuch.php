<h2>Laura Cuch</h2>

<h3 class="title">Sleepless</h3>

<p>This project consists of a series of portraits of people who for some reason sleep very little. This work reflects on a surprising fact, that such everyday things as sleeping and dreams are still much of a mystery. From a scientific point of view, it’s not really known what the benefits of sleep are for our metabolism and what are the consequences of its lack. From a cultural point of view, it’s interesting to look at how the act of sleeping is understood, because it leads us to observe how a society relates to the notion of time and its control, night and day, privacy, safety and surveillance, consciousness and oblivion, the notion of health and the definition of pathologies.</p>

<p>I am interested in looking at lack of sleep as a phenomenon, without limiting it to a specific reason, and she sets the selection criteria that the people portrayed sleep under four hours per day, even if it is temporarily. Through this work, I attempt to understand portraiture as an affective collaborative process. I have interviewed my subjects and negotiated with them both the elements that will appear in the portraits (such as objects and locations), and the biographical texts that accompany the images.</p>

<h3>Biography</h3>

<p>Laura Cuch is a documentary and fine art photographer as well as a researcher at the Centre for Urban and Community Research at Goldsmiths, University of London. She currently teaches in the MA in Photography and Urban Cultures and the Urban Photography Summer School, also at Goldsmiths. In 2011 Laura was a research fellow in the Department of Anthropology at Goldsmiths, which led to a solo exhibition and the conference ‘Negotiating Subjectivities: A One-Day Symposium on Photography, Health and the Body’. Laura has been working as a freelance photographer since 2004 and her work has been exhibited and published internationally.</p>

