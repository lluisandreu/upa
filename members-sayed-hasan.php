<h2>Sayed Hasan </h2>

<h3 class="title">Surrender Your Gaze</h3>

<p>As one walks through the city it is common to see people - from tourists to professionals - carrying cameras, on the look out for something to photograph. </p>

<p>Standing in wait on Ramillies Street in an urban clearing between Oxford Street and The Photographers gallery- I stopped such individuals as they strolled by. “Can I take your photograph?”, I would ask. Then quickly add - before an answer was given- “on one condition... that I blindfold you!”.</p>

<p>There is a black humour in the thought of shooting one photographer after another against a wall, but it wasn’t intended as an act of aggression. I simply asked the strangers I approached to disengage from the act of looking for a brief moment.</p>

<h3>Biography</h3>

<p>The Artist’s visual practice is an on-going dialogue with everyday life reflecting and responding to themes around family, religion, the body and social interaction which is represented through photography, video and installation.</p>

<p>Since graduating in 2009 in MA Photography and Urban Cultures, Sayed Hasan has exhibited in galleries and festivals nationally and internationally and been awarded funding from Arts Council England to develop my work.</p>


