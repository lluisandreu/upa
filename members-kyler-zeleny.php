	<h2>Kyler Zeleny</h2>
	
	<h3 class="title">Out West</h3>
	
	<p>This is an ongoing project documenting small rural communities (1,000 inhabitants or less) in the Canadian West. The project was conducted to explore what was perceived as a lack of imagery on the current state of the rural in the Canadian West, as well as, a need to document the continued transition from rural-dominant (western) to urban-dominant provinces—rural drain, urban claim.</p>
	
	<p>In a liminal sense, rural spaces are experiencing a time lag, a division between the rural and urban partly created by the diminishment of rural areas and favorable views of urban spaces. This phenomena results in the images conjuring up a Vonnegut-like idea of being “unstuck in time”, where objects and the built landscape deceive the viewer as to what period they belong too; visualized by aged vehicles, faded signage, dated house siding, or any other number of visual cues used to determine the age of a scene.</p>
	
	<h3>Biography</h3>
	
	<p>Kyler Zeleny is a Canadian visual sociologist. He is interested in Found Images from family albums, conscious/unconscious construction of ‘deviant cultures’, alternative culture movements, and how this defines and influences social policies. His personal interests in photography relate to open space, landscape portraiture, and the archeology of rural decay, which is reflective of his rural upbringing.</p> 
	
	<p>He received his bachelors in Political Science from the University of Alberta and his masters in Sociology at Goldsmiths College, London in Photography and Urban Cultures. He is an editor with the publishing house The Velvet Cell. Currently he is preparing to start a PhD.</p>
	
